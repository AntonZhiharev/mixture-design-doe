"""Лёгкие admin-хелперы для деструктивных операций UI (удаление проекта).

ВАЖНО про модель угроз: Streamlit исполняется на сервере, проверка пароля тоже
серверная — это **барьер от случайного удаления** обычным пользователем, а НЕ
криптографическая защита. Пароль не секретный (репозиторий открытый): дефолт
лежит константой в коде, при необходимости переопределяется переменной
окружения ``DOE_ADMIN_PASSWORD`` (например, на общем сервере).

Отдельной «админки»/ролей сознательно нет — для единственной опасной операции
это было бы избыточно (соразмерность). Если опасных операций станет больше —
сюда же добавятся их проверки.
"""
from __future__ import annotations

import os

# Дефолт-пароль (НЕ секрет): барьер от случайного удаления. Переопределяется
# переменной окружения DOE_ADMIN_PASSWORD.
DEFAULT_ADMIN_PASSWORD = "doe-admin"
ADMIN_PASSWORD_ENV = "DOE_ADMIN_PASSWORD"


def admin_password() -> str:
    """Текущий admin-пароль: из env ``DOE_ADMIN_PASSWORD`` либо дефолт-константа.

    Пустое/незаданное значение env трактуется как «не задано» → берётся дефолт.
    """
    env = os.environ.get(ADMIN_PASSWORD_ENV)
    return env if env else DEFAULT_ADMIN_PASSWORD


def check_admin_password(entered: str | None) -> bool:
    """Проверить введённый пароль на совпадение с текущим admin-паролём.

    Пустой ввод всегда неверен (чтобы не разрешать удаление «пустым» паролём).
    """
    if not entered:
        return False
    return str(entered) == admin_password()
